#
for INDEX in $(seq 1 4 $MAX_SUBSET_INDEX)  
  do 
    ind=$(($INDEX + 0))
    if (( ind <= $MAX_SUBSET_INDEX)); then
        echo "Starting oger pipeline... ${ind} $(date)"
        COLLECTION="${SUBSET_PREFIX}${ind}"
        $SCRIPT $PROJECT_ID ${COLLECTION} ${STAGE_LOCATION} ${TEMP_LOCATION} ${WORK_BUCKET} $TEXT_PIPELINE_KEY $TEXT_PIPELINE_VERSION $AUGMENTED_TEXT_PIPELINE_KEY $AUGMENTED_TEXT_PIPELINE_VERSION $OGER_PP_PIPELINE_VERSION $CRF_PIPELINE_VERSION $ABBREV_PIPELINE_VERSION $FILTER_FLAG $JAR_VERSION $OUTPUT_PIPELINE_VERSION &> "./logs/concept-post-process-${COLLECTION}.log" &
        sleep 120
    fi
    ind=$(($INDEX + 1))
    if (( ind <= $MAX_SUBSET_INDEX)); then
        echo "Starting oger pipeline... ${ind} $(date)"
        COLLECTION="${SUBSET_PREFIX}${ind}"
        $SCRIPT $PROJECT_ID ${COLLECTION} ${STAGE_LOCATION} ${TEMP_LOCATION} ${WORK_BUCKET} $TEXT_PIPELINE_KEY $TEXT_PIPELINE_VERSION $AUGMENTED_TEXT_PIPELINE_KEY $AUGMENTED_TEXT_PIPELINE_VERSION $OGER_PP_PIPELINE_VERSION $CRF_PIPELINE_VERSION $ABBREV_PIPELINE_VERSION $FILTER_FLAG $JAR_VERSION $OUTPUT_PIPELINE_VERSION &> "./logs/concept-post-process-${COLLECTION}.log" &
        sleep 120
    fi
    ind=$(($INDEX + 2))
    if (( ind <= $MAX_SUBSET_INDEX)); then
        echo "Starting oger pipeline... ${ind} $(date)"
        COLLECTION="${SUBSET_PREFIX}${ind}"
        $SCRIPT $PROJECT_ID ${COLLECTION} ${STAGE_LOCATION} ${TEMP_LOCATION} ${WORK_BUCKET} $TEXT_PIPELINE_KEY $TEXT_PIPELINE_VERSION $AUGMENTED_TEXT_PIPELINE_KEY $AUGMENTED_TEXT_PIPELINE_VERSION $OGER_PP_PIPELINE_VERSION $CRF_PIPELINE_VERSION $ABBREV_PIPELINE_VERSION $FILTER_FLAG $JAR_VERSION $OUTPUT_PIPELINE_VERSION &> "./logs/concept-post-process-${COLLECTION}.log" &
        sleep 120
    fi
    ind=$(($INDEX + 3))
    if (( ind <= $MAX_SUBSET_INDEX)); then
        echo "Starting oger pipeline... ${ind} $(date)"
        COLLECTION="${SUBSET_PREFIX}${ind}"
        $SCRIPT $PROJECT_ID ${COLLECTION} ${STAGE_LOCATION} ${TEMP_LOCATION} ${WORK_BUCKET} $TEXT_PIPELINE_KEY $TEXT_PIPELINE_VERSION $AUGMENTED_TEXT_PIPELINE_KEY $AUGMENTED_TEXT_PIPELINE_VERSION $OGER_PP_PIPELINE_VERSION $CRF_PIPELINE_VERSION $ABBREV_PIPELINE_VERSION $FILTER_FLAG $JAR_VERSION $OUTPUT_PIPELINE_VERSION &> "./logs/concept-post-process-${COLLECTION}.log" &
    fi
    wait 
  done
