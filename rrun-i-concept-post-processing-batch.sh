AUGMENTED_TEXT_PIPELINE_KEY="DOC_TEXT_AUGMENTATION"
AUGMENTED_TEXT_PIPELINE_VERSION="recent"
OGER_PP_PIPELINE_VERSION="recent"
ABBREV_PIPELINE_VERSION="recent"
